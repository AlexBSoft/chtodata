def _normalize_region_key(region: str) -> str:
    """Приводит название региона к ключу карты таймзон.

    Снимает суффиксы-уточнения («Кемеровская область - Кузбасс» -> «Кемеровская область»,
    «Ханты-Мансийский автономный округ - Югра» -> «Ханты-Мансийский автономный округ»,
    «Чувашская Республика - Чувашия» -> «Чувашская Республика»).
    """
    key = region.strip()
    # Разрезаем по « - » / « — » (дефис или тире с пробелами).
    key = re.split(r"\s+[-—]\s+", key)[0].strip()
    return key


def region_timezone(region: str) -> str:
    """UTC-смещение по названию региона, например «UTC+4». Пустая строка, если неизвестно."""
    if not region:
        return ""
    return _TIMEZONES.get(_normalize_region_key(region), "")
