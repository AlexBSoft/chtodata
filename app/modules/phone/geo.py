"""Гео-логика: разбор «Территория ГАР» -> (регион, город), карта таймзон, тип номера.

Данные берутся из реестра Минцифры, поэтому значения — best-effort и могут немного
отличаться от других сервисов (например, DaData использует собственные геобазы).
"""

from __future__ import annotations

import re

# --- Разбор населённого пункта -------------------------------------------------

# Префиксы типов населённых пунктов, которые нужно срезать, чтобы получить «голое»
# название города. Порядок важен: более длинные варианты идут первыми.
_LOCALITY_PREFIX = re.compile(
    r"^(?:"
    r"г\.о\.\s*город|городской\s+округ|г\.о\.|"
    r"город|г\.|"
    r"пгт\.?|п\.г\.т\.?|"
    r"р\.?п\.?|рабочий\s+посёлок|рабочий\s+поселок|"
    r"пос\.?|посёлок|поселок|п\.|"
    r"село|с\.|"
    r"деревня|д\.|"
    r"станица|ст-ца|ст\.|"
    r"хутор|х\.|"
    r"аул"
    r")\s+",
    re.IGNORECASE,
)

# Федеральные города: в реестре записаны как «Город Москва» и т.п.; для них регион == город.
_FEDERAL_CITIES = {
    "москва": "Москва",
    "санкт-петербург": "Санкт-Петербург",
    "севастополь": "Севастополь",
    "байконур": "Байконур",
}


def _strip_locality_prefix(name: str) -> str:
    """Срезает ведущие типы населённого пункта: «г.о. город Калининград» -> «Калининград»."""
    prev = None
    cur = name.strip()
    # Цикл, т.к. префиксов может быть несколько («г.о.» + «город»).
    while cur and cur != prev:
        prev = cur
        cur = _LOCALITY_PREFIX.sub("", cur, count=1).strip()
    return cur


def _federal_city(segment: str) -> str | None:
    """«Город Москва» -> «Москва», если это федеральный город, иначе None."""
    bare = re.sub(r"^город\s+", "", segment.strip(), flags=re.IGNORECASE).strip()
    return _FEDERAL_CITIES.get(bare.lower())


def parse_territory(territory: str, region_raw: str = "") -> tuple[str, str]:
    """Возвращает (регион, город) из колонки «Территория ГАР».

    Форматы в реестре:
      * «г.о. Самара|Самарская область»                  -> («Самарская область», «Самара»)
      * «г.о. город Калининград|Калининградская область» -> («Калининградская область», «Калининград»)
      * «г. Улан-Удэ|г.о. город Улан-Удэ|Республика Бурятия» -> («Республика Бурятия», «Улан-Удэ»)
      * «Город Москва»                                   -> («Москва», «Москва»)
      * «Краснодарский край» (мобильный, регион-только)  -> («Краснодарский край», «»)
      * «Республика Адыгея, ... » (8-800, список регионов) -> («», «»)
    """
    t = (territory or "").strip()
    if not t:
        t = (region_raw or "").strip()
    if not t:
        return ("", "")

    if t in ("Российская Федерация", "Россия"):
        return ("", "")

    # Перечисление субъектов через запятую (нет иерархии с «|»).
    if "|" not in t and "," in t:
        items = [p.strip() for p in t.split(",") if p.strip()]
        first = items[0] if items else ""
        # Небольшой блок (мобильный диапазон на 2-3 смежных региона) — берём первый субъект.
        # Большой список (8-800 на всю страну) или «Российская Федерация, кроме …» — региона нет.
        if 2 <= len(items) <= 3 and not first.startswith(("Российская Федерация", "Россия")):
            return (_federal_city(first) or first, "")
        return ("", "")

    parts = [p.strip() for p in t.split("|") if p.strip()]
    if not parts:
        return ("", "")

    if len(parts) == 1:
        seg = parts[0]
        fed = _federal_city(seg)
        if fed:
            return (fed, fed)
        # Это субъект РФ без указания населённого пункта (типично для мобильных).
        return (seg, "")

    region = parts[-1]
    fed = _federal_city(region)
    if fed:
        region = fed
    city = _strip_locality_prefix(parts[0])
    return (region, city)


# --- Тип номера ---------------------------------------------------------------


def phone_type(code: int, kind: str) -> str:
    """Тип российского номера для поля ``type`` в стиле DaData."""
    if kind == "DEF":
        return "Мобильный"
    if code in (800, 804):
        return "Бесплатный"
    return "Стационарный"


# Соответствие типов phonenumbers -> русские подписи (для международных номеров).
def type_ru_from_phonenumbers(number_type: int) -> str:
    import phonenumbers

    mapping = {
        phonenumbers.PhoneNumberType.MOBILE: "Мобильный",
        phonenumbers.PhoneNumberType.FIXED_LINE: "Стационарный",
        phonenumbers.PhoneNumberType.FIXED_LINE_OR_MOBILE: "Стационарный",
        phonenumbers.PhoneNumberType.TOLL_FREE: "Бесплатный",
        phonenumbers.PhoneNumberType.PREMIUM_RATE: "Платный",
        phonenumbers.PhoneNumberType.VOIP: "IP-телефония",
        phonenumbers.PhoneNumberType.SHARED_COST: "Стационарный",
        phonenumbers.PhoneNumberType.PERSONAL_NUMBER: "Стационарный",
        phonenumbers.PhoneNumberType.PAGER: "Пейджер",
        phonenumbers.PhoneNumberType.UAN: "Стационарный",
    }
    return mapping.get(number_type, "")


